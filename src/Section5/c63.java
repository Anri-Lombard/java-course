package Section5;

public class c63 {
    public static void main(String[] args) {
        char letter = 'C';

        switch(letter) {
            case 'A':
                System.out.println("Letter is A");
            break;

            case 'B':
                System.out.println("Letter is B");
            break;

            default:
                System.out.println("Not an A or a B");
            break;
        }
    }
}
